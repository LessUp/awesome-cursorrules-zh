import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const docsRoot = path.resolve(__dirname, '..')
const rulesRoot = path.join(docsRoot, 'rules')
const outputFile = path.join(docsRoot, '.vitepress', 'site', 'facts.ts')

function walkRuleFiles(root) {
  const files = []

  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    const fullPath = path.join(root, entry.name)
    if (entry.isDirectory()) {
      files.push(...walkRuleFiles(fullPath))
      continue
    }

    if (entry.isFile() && entry.name === '.cursorrules') {
      files.push(fullPath)
    }
  }

  return files
}

const ruleFiles = walkRuleFiles(rulesRoot)
const categories = fs
  .readdirSync(rulesRoot, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)

const countsByCategory = categories
  .map((category) => ({
    category,
    count: ruleFiles.filter((file) => file.startsWith(path.join(rulesRoot, category))).length,
  }))
  .sort((left, right) => right.count - left.count || left.category.localeCompare(right.category))

const categoryDistribution = countsByCategory.map(({ category, count }) => ({
  category,
  count,
  ratio: Number((count / ruleFiles.length).toFixed(4)),
}))

const coverageBuckets = [
  {
    label: 'High-density domains',
    count: countsByCategory.filter(({ count }) => count >= 8).length,
  },
  {
    label: 'Mid-density domains',
    count: countsByCategory.filter(({ count }) => count >= 4 && count < 8).length,
  },
  {
    label: 'Long-tail domains',
    count: countsByCategory.filter(({ count }) => count < 4).length,
  },
]

const qualitySignals = {
  dominantCategory: countsByCategory[0]?.category ?? null,
  dominantCategoryCount: countsByCategory[0]?.count ?? 0,
  curatedSurfaceCount: countsByCategory.filter(({ count }) => count >= 4).length,
  longTailCategoryCount: countsByCategory.filter(({ count }) => count <= 2).length,
}

const timelineSignals = [
  {
    phase: 'Asset curation',
    focus: 'Normalize the rules corpus into a stable taxonomy.',
  },
  {
    phase: 'Narrative orchestration',
    focus: 'Translate repository structure into explainable architecture and algorithm chapters.',
  },
  {
    phase: 'Evidence publication',
    focus: 'Render metrics, references, and diagrams through the VitePress shell.',
  },
]

const siteFacts = {
  ruleCount: ruleFiles.length,
  domainCount: categories.length,
  localeCount: 2,
  topDomains: countsByCategory.slice(0, 6),
  categoryDistribution,
  coverageBuckets,
  qualitySignals,
  timelineSignals,
}

const fileContent = `/* This file is generated by docs/scripts/sync-site-facts.mjs */\nexport const siteFacts = ${JSON.stringify(siteFacts, null, 2)} as const\n`

fs.writeFileSync(outputFile, fileContent)
console.log(`Synced site facts to ${path.relative(docsRoot, outputFile)}`)
