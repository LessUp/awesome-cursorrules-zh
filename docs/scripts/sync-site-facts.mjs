import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const docsRoot = path.resolve(__dirname, '..')
const rulesRoot = path.join(docsRoot, 'rules')
const outputFile = path.join(docsRoot, '.vitepress', 'site', 'facts.ts')

function walkRuleFiles(root) {
  const files = []

  for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
    const fullPath = path.join(root, entry.name)
    if (entry.isDirectory()) {
      files.push(...walkRuleFiles(fullPath))
      continue
    }

    if (entry.isFile() && entry.name === '.cursorrules') {
      files.push(fullPath)
    }
  }

  return files
}

const ruleFiles = walkRuleFiles(rulesRoot)
const categories = fs
  .readdirSync(rulesRoot, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)

const countsByCategory = categories
  .map((category) => ({
    category,
    count: ruleFiles.filter((file) => file.startsWith(path.join(rulesRoot, category))).length,
  }))
  .sort((left, right) => right.count - left.count || left.category.localeCompare(right.category))

const siteFacts = {
  ruleCount: ruleFiles.length,
  domainCount: categories.length,
  localeCount: 2,
  topDomains: countsByCategory.slice(0, 6),
}

const fileContent = `/* This file is generated by docs/scripts/sync-site-facts.mjs */\nexport const siteFacts = ${JSON.stringify(siteFacts, null, 2)} as const\n`

fs.writeFileSync(outputFile, fileContent)
console.log(`Synced site facts to ${path.relative(docsRoot, outputFile)}`)
